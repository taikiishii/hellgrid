// E1M5「水路」の実戦ステージを組み立てて map 配列を出力する: node tools/gen-e1m5.js
// 出力を levels.js の E1M5 の map に貼り付ける。25x19。
const W = 25, H = 19;
const g = Array.from({ length: H }, () => new Array(W).fill('#'));
const room = (x0, y0, x1, y1) => { for (let y = y0; y <= y1; y++) for (let x = x0; x <= x1; x++) g[y][x] = '.'; };
const put = (x, y, c) => { g[y][x] = c; };
const rect = (x0, y0, x1, y1, c) => { for (let y = y0; y <= y1; y++) for (let x = x0; x <= x1; x++) g[y][x] = c; };

// ---- 部屋を彫る ----
room(8, 4, 16, 12);   // 中央ホール(運河)
room(8, 1, 16, 2);    // 北の出口部屋(青ドアの先)
room(9, 14, 15, 17);  // 南のスタート部屋
room(1, 4, 6, 12);    // 西の部屋
room(18, 4, 23, 12);  // 東の部屋
room(2, 1, 4, 2);     // 隠し部屋(西の上)

// ---- ドア ----
put(12, 3, 'B');      // 青ドア(出口部屋へ)
put(12, 13, 'D');     // スタート→中央
put(7, 8, 'D');       // 中央→西
put(17, 8, 'D');      // 中央→東
put(3, 3, '*');       // 隠し扉(西の部屋→隠し部屋)

// ---- 水路・池(中央運河、上下2つの池) ----
rect(10, 5, 14, 6, '~');   // 北の池
rect(10, 10, 14, 11, '~'); // 南の池

// ---- 動く歩道(中央を東へ横断する歩道。col9 は縦移動レーンとして空ける) ----
rect(10, 8, 15, 8, '>');
// 西の部屋: 南北に走る動く歩道(北へ運ぶ)で素早く移動
rect(5, 5, 5, 11, '^');

// ---- スタート / 出口 ----
put(12, 16, 'P');
put(12, 0, 'X');      // 北壁の出口スイッチ

// ---- 敵(大文字=最初からいる / 小文字=休眠) ----
put(12, 1, 'K');      // 出口を守るヘルナイト(ボス)
put(2, 5, 'G'); put(3, 10, 'I');           // 西
put(9, 4, 'Z'); put(15, 12, 'Z');          // 中央
put(18, 11, 'G');                          // 東(常駐)
put(19, 5, 'g'); put(22, 9, 'i');          // 東(休眠=青キーの罠)
put(11, 9, 'M');                           // 運河際のデーモン

// ---- アンブッシュtrigger(青キーの近く) ----
put(21, 7, 'T');

// ---- アイテム ----
put(11, 16, 'a'); put(13, 16, 'h');        // スタート
put(3, 8, 'S'); put(2, 11, 'A'); put(6, 5, 's'); // 西(ショットガン)
put(2, 1, 'H'); put(4, 1, 'A');            // 隠し部屋
put(21, 6, 'b'); put(22, 11, 'A'); put(18, 5, 'H'); // 東(青キー)
put(9, 11, 's');                           // 中央
put(9, 1, 'H'); put(15, 1, 'a');           // 出口部屋

// 出力
console.log('    map: [');
for (const row of g) console.log(`      '${row.join('')}',`);
console.log('    ],');
