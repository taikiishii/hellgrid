/* =========================================================================
 * レベルデータ定義
 *
 * マップは文字列の配列。1文字 = 1タイル。すべての行は同じ長さにすること
 * (node tools/check-maps.js で検証できます)。新しいステージを追加するには、
 * この LEVELS 配列にオブジェクトを足すだけでOK。
 *
 * ---- 凡例 ----
 *  壁:
 *   '#'  レンガ壁
 *   '&'  テック壁(パネル)
 *   '='  石柱
 *   'D'  ドア(Eキーで開く)
 *   'R'  赤キーカードが必要なロックドア
 *   'B'  青キーカードが必要なロックドア
 *   '*'  隠し扉(見た目は壁。Eキーで開く。シークレット扱い)
 *   'X'  出口スイッチ(Eキーでステージクリア)
 *  床:
 *   '.'  通常床
 *   'P'  プレイヤー開始位置
 *   'T'  アンブッシュトリガー(踏むと付近の休眠敵が出現する。見えない)
 *   '~'  水路・池(通行不可。床より低い窪みとして描かれる。弾は上を通過)
 *   '>' '<' '^' 'v'  動く歩道(乗るとその向きへ流される。^=北/奥, v=南/手前)
 *   'L'  リフト(時間で上下する床。低い所と高い所を結ぶ。全リフト同期)
 *   'o'  爆発樽(撃つと誘爆し、爆風で周囲の敵やプレイヤーを巻き込む。連鎖あり)
 *  敵 (大文字=最初からいる、小文字=休眠中でトリガーで出現):
 *   'Z'/'z'  亡兵(銃撃・弱い)
 *   'G'/'g'  散弾兵(散弾・シェルを落とす)
 *   'I'/'i'  焔鬼(火球を投げる)
 *   'M'/'m'  牙獣(高速で突進して噛みつく)
 *   'K'/'k'  獄騎士(タフ・速い緑火球)
 *   'F'/'f'  漂霊(空中を漂い遠距離から撃つ。heights で高い位置に置くと映える)
 *  アイテム:
 *   'h'  スティムパック (HP+10)
 *   'H'  メディキット (HP+25)
 *   'a'  弾倉 (弾+10)
 *   'A'  弾薬箱 (弾+25)
 *   's'  シェル (シェル+4)
 *   'S'  ショットガン (武器+シェル8)
 *   'r'  赤キーカード
 *   'b'  青キーカード
 *   'p'  アーマーの破片 (アーマー+5)
 *   'V'  ボディアーマー (アーマー+100)
 *
 * ---- 立体ステージ用の追加フィールド (すべて任意) ----
 *  heights:    マップと同じ縦横サイズの文字列配列。'0'-'9' = 床の高さ
 *              (0.25タイル刻み。'4' = 1タイルぶんの高台)。省略時は全面フラット。
 *              高低差 0.5 ('2'相当) までは歩いて登れる。それより高い段差は
 *              壁として機能する(階段は 0→1→2→3→4 のように刻む)。
 *              敵は登れる段差しか移動できず、高所から飛び降りない。
 *  ceilHeight: 天井の高さ(タイル単位の整数、省略時1)。高台を作るなら2を推奨。
 *  stepTex:    段差側面のテクスチャ文字(省略時 '=')
 * ========================================================================= */

