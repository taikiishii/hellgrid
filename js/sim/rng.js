'use strict';
/* =========================================================================
 * 決定的な擬似乱数 (mulberry32)
 *
 * シミュレーションは Math.random() を一切使わず、World が持つこの RNG だけを
 * 使う。同じシード + 同じ入力列 -> 同じ結果、が保証される(学習・リプレイ・
 * パリティテストの前提)。
 * ========================================================================= */
function makeRNG(seed) {
  let a = (seed >>> 0) || 1;
  return function rng() {
    a = (a + 0x6D2B79F5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

globalThis.makeRNG = makeRNG;
